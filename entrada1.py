print('Digite o primeiro número: ') # primeiro número
num1 = float(input()) # na variável num1 eu vou armazenar o input, ou seja, o que vai entrar após o pedido de digitação
# esse input, será convertido em floar

print('Digite o segundo número: ')
num2 = float(input()) # na variável num2 eu vou armazenar o input, ou seja, o que vai entrar após o pedido de digitação
# esse input, será convertido em floar

# executar operações
add = num1 + num2 # adição
sub = num1 - num2 # subtração
mul = num1 * num2 # multiplicação
div = num1 / num2 # divisão


# mostrar resultados
print(f'A soma de {num1} e {num2} é {add}')
print(f'A subtração de {num1} e {num2} é {sub}')
print(f'A multiplicação de {num1} e {num2} é {mul}')
print(f'A divisão de {num1} e {num2} é {div}')



